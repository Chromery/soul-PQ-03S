import { createReadStream } from "node:fs";
import { mkdir, writeFile } from "node:fs/promises";
import { execFileSync } from "node:child_process";
import { Readable } from "node:stream";
import { createInterface } from "node:readline";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const DEFAULT_SOURCE = "https://raw.githubusercontent.com/Chromery/soul-delibere-rk/main/estratti/estrazioni.jsonl";
const outputPath = fileURLToPath(new URL("../src/imu/imu-rates.generated.ts", import.meta.url));
const inputArgument = process.argv.find((argument) => argument.startsWith("--input="))?.slice("--input=".length);
const source = inputArgument ?? process.env.IMU_EXTRACTIONS_SOURCE ?? DEFAULT_SOURCE;
const input = await openInput(source);
const latestByMunicipalityYear = new Map();
let extractedDocuments = 0;

for await (const line of createInterface({ input, crlfDelay: Infinity })) {
  if (!line.trim()) continue;
  const document = JSON.parse(line);
  if (document.status !== "OK") continue;
  const record = normalizeDocument(document);
  if (!record) continue;
  extractedDocuments++;
  const key = `${record.cadastralCode}:${record.year}`;
  const previous = latestByMunicipalityYear.get(key);
  if (!previous || documentTimestamp(record) >= documentTimestamp(previous)) {
    latestByMunicipalityYear.set(key, record);
  }
}

const records = Array.from(latestByMunicipalityYear.values()).sort((first, second) =>
  first.cadastralCode.localeCompare(second.cadastralCode) || second.year - first.year,
);
const content = `import type { ImuRateRecord } from "./imu.types.js";\n\n`
  + `// Generated by npm run imu:sync. Do not edit manually.\n`
  + `export const IMU_RATE_DATA_SOURCE = ${JSON.stringify(source)};\n`
  + `export const IMU_RATE_RECORDS: ImuRateRecord[] = ${JSON.stringify(records)};\n`;
await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, content);

const standard = records.filter((record) => record.documentType === "mef_standard_prospect");
const fallback = records.filter((record) => record.year === 2025).length;
const groupDCoverage = standard.filter((record) => record.groupDRate !== null).length;
process.stdout.write(
  `IMU rates generated: ${records.length} municipality/year records from ${extractedDocuments} documents; `
  + `${fallback} fallback 2025; group D coverage ${groupDCoverage}/${standard.length}.\n`,
);

async function openInput(value) {
  if (!/^https?:\/\//i.test(value)) return createReadStream(resolve(value), "utf8");
  const headers = { "User-Agent": "soul-pq-imu-sync" };
  const token = githubToken();
  if (token) headers.Authorization = `Bearer ${token}`;
  const response = await fetch(value, { headers });
  if (!response.ok || !response.body) throw new Error(`Unable to download IMU extractions: HTTP ${response.status}`);
  return Readable.fromWeb(response.body);
}

function githubToken() {
  const configured = process.env.GITHUB_TOKEN ?? process.env.GH_TOKEN;
  if (configured) return configured;
  try {
    return execFileSync("gh", ["auth", "token"], { encoding: "utf8", stdio: ["ignore", "pipe", "ignore"] }).trim();
  } catch {
    return "";
  }
}

function normalizeDocument(document) {
  const source = document.source ?? {};
  const year = Number(source.ANNO_FONTE);
  if (!source.CODICE_CATASTALE || !source.COMUNE || !Number.isInteger(year)) return null;
  const rows = Array.isArray(document.table_rows) ? document.table_rows : [];
  return {
    cadastralCode: String(source.CODICE_CATASTALE).trim().toUpperCase(),
    municipality: String(source.COMUNE).trim(),
    province: String(source.PR ?? "").trim().toUpperCase(),
    region: String(source.REGIONE ?? "").trim(),
    year,
    documentType: document.document_type === "mef_standard_prospect" ? "mef_standard_prospect" : "municipal_resolution",
    groupDRate: findRate(rows, (category, detail) =>
      category.includes("fabbricati appartenenti al gruppo catastale d")
      && category.includes("esclusa")
      && detail === "",
    ),
    ruralInstrumentalRate: findRate(rows, (category, detail) =>
      category.includes("fabbricati rurali ad uso strumentale") && detail === "",
    ),
    otherBuildingsRate: findRate(rows, (category, detail) => category.includes("altri fabbricati") && detail === ""),
    actNumber: String(source.NUMERO ?? document.standard_fields?.act_number ?? "").trim(),
    actDate: String(source.DATA_DELIBERA ?? document.standard_fields?.act_date ?? "").trim(),
    publicationDate: String(source.DATA_PUBBLICAZIONE ?? "").trim(),
    sourcePath: String(source.PERCORSO ?? "").trim(),
    sha256: String(source.SHA256 ?? "").trim(),
  };
}

function findRate(rows, predicate) {
  for (const row of rows) {
    const category = normalizeText(row.category);
    const detail = normalizeText(row.detail);
    if (!predicate(category, detail)) continue;
    const match = String(row.value ?? "").trim().match(/^(\d+(?:[.,]\d+)?)\s*%$/);
    if (match) return Number(match[1].replace(",", "."));
  }
  return null;
}

function normalizeText(value) {
  return String(value ?? "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\s+/g, " ")
    .trim()
    .toLowerCase();
}

function documentTimestamp(record) {
  return parseItalianDate(record.publicationDate) || parseItalianDate(record.actDate) || 0;
}

function parseItalianDate(value) {
  const match = String(value).match(/^(\d{1,2})[/-](\d{1,2})[/-](\d{4})$/);
  return match ? Date.UTC(Number(match[3]), Number(match[2]) - 1, Number(match[1])) : 0;
}
